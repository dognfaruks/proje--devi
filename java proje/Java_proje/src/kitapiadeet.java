    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        yazi.setText("");
        int secilen =kitaptablosu.getSelectedRow();
        if(secilen == -1){
            yazi.setText("Lütfen İade Edilecek Kitabı Seçiniz");
        } 
        else{
            int id = (int)model.getValueAt(secilen, 0);
            try {
                islemler.iadeet(id);
            } catch (SQLException ex) {
                Logger.getLogger(kitapiadeet.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                listegoruntule();
            } catch (SQLException ex) {
                Logger.getLogger(kitapiadeet.class.getName()).log(Level.SEVERE, null, ex);
            }
            yazi.setText("Kitap Basarıyla İade Edildi");}
            adcubugu.setText("");
            kitapadicubugu.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

