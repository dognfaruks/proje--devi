    public void listegoruntule() throws SQLException{
        model.setRowCount(0);
        ArrayList<kitap> kitaplar = new ArrayList<kitap>();
        kitaplar = islemler.kitapgoruntule();
        if(kitaplar !=null){
            for(kitap kitap:kitaplar){
                
                Object[] eklenecek ={kitap.getId(),kitap.getAd(),kitap.getKitap()};
                model.addRow(eklenecek);
        }}
        
        
    }

